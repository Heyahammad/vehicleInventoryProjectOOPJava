    private void loadCarDataFromFile() {
        String filePath = "carData.txt";
        dtm = (DefaultTableModel) CarDataTable.getModel();
        dtm.setRowCount(0);
        vl.clear();

        try (BufferedReader reader = new BufferedReader(new FileReader(filePath))) {
            String currentLine;

            while ((currentLine = reader.readLine()) != null) {
                String[] data = currentLine.split(",, ");
                String regNo = data[0].split(": ")[1].trim();
                String brand = data[1].split(": ")[1].trim();
                String model = data[2].split(": ")[1].trim();
                String color = data[3].split(": ")[1].trim();
                String status = data[4].split(": ")[1].trim();
                String dom = data[5].split(": ")[1].trim();
                String condition = data[6].split(": ")[1].trim();
                Object[] rowData = {regNo, brand, model, color, status, dom, condition};
                dtm.addRow(rowData);

                Car car = new Car(regNo, brand, model, color, status, dom, condition);
                vl.add(car);
            }
        } catch (IOException e) {
            System.out.println("" + e);
        }
    }
