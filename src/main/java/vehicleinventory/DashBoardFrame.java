    private void saveTableDataToTextFile() throws IOException {
        String filePath = "carData.txt";
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(filePath))) {
            for (int row = 0; row < dtm.getRowCount(); row++) {
                String regNo = dtm.getValueAt(row, 0).toString();
                String brand = dtm.getValueAt(row, 1).toString();
                String model = dtm.getValueAt(row, 2).toString();
                String color = dtm.getValueAt(row, 3).toString();
                String status = dtm.getValueAt(row, 4).toString();
                String dom = dtm.getValueAt(row, 5).toString();
                String condition = dtm.getValueAt(row, 6).toString();

                String line = String.format("Reg No: " + regNo + ",, " + "Brand: " + brand + ",, " + 
                        "Model: " + model + ",, " + "Color: " + color + ",, " + "Status: " + status + ",, " + "Dom: " + dom + ",, " + "Condition: " + condition);
                writer.write(line);
                writer.newLine();
            }
        }
    }
