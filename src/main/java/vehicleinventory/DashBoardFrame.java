    private void CarDataTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CarDataTableMouseClicked

        try {

            int i = CarDataTable.getSelectedRow();
            String a = dtm.getValueAt(i, 0).toString();
            String b = dtm.getValueAt(i, 1).toString();
            String c = dtm.getValueAt(i, 2).toString();
            String d = dtm.getValueAt(i, 3).toString();
            String e = dtm.getValueAt(i, 4).toString();
            String f = dtm.getValueAt(i, 5).toString();
            String g = dtm.getValueAt(i, 6).toString();

            regNoBox.setText(a);
            brandBox.setText(b);
            modelBox.setText(c);
            colorBox.setText(d);
            if (e.equalsIgnoreCase("Rented")) {
                status1.setSelected(true);
            } else {
                status2.setSelected(true);
            }
            Date D = new SimpleDateFormat("MMM d, y").parse((String) f.toString());
            domBox.setDate(D);
            conditionBox.setSelectedItem(g);
        }
        catch (Exception e) {
            System.out.println("" + e);
        }
    }//GEN-LAST:event_CarDataTableMouseClicked

